    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {                                             
            // TODO add your handling code here:
            InfoDao dao = new InfoDao();
            String fId = txtID.getText();
            int id = Integer.parseInt(fId);
            Info info = dao.findById(id);
            
            if(info != null){
               txtNome.setText(info.getNome());
               txtApelido.setText(info.getApelido());
               txtDataNascimento.setText(info.getDataNascimento().toString());
               txtEmail.setText(info.getEmail());
               txtTelefone.setText(info.getTelefone());
            }
            else
                JOptionPane.showMessageDialog(null, "Resultado n√£o encontrado");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            // TODO add your handling code here:
            Info info = new Info();
            InfoDao dao = new InfoDao();
            String dataNasci = txtDataNascimento.getText();
            
            LocalDate dataNasciemnto = LocalDate.parse(dataNasci,DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            
            
            info.setNome(txtNome.getText());
            info.setApelido(txtApelido.getText());
            info.setDataNascimento(dataNasciemnto);
            info.setEmail(txtEmail.getText());
            info.setTelefone(txtTelefone.getText());
            dao.addNew(info);
        } catch (SQLException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtApelido.setText("");
        txtDataNascimento.setText("");
        txtEmail.setText("");
        txtID.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
         DefaultTableModel modelo = (DefaultTableModel) tbContatos.getModel();
        modelo.setNumRows(0);
        InfoDao dao = new InfoDao();
        try {
            for(Info i :dao.findAll()){
                modelo.addRow(new Object[]{i.getId(),i.getNome(),i.getApelido(),i.getDataNascimento(),i.getEmail(),i.getTelefone()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

